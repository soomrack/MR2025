#ifndef TYPES_H
#define TYPES_H

// ============================================================================
// ОБЩИЕ КОНСТАНТЫ
// ============================================================================

#define MAX_CLIENTS  10
#define NAME_LEN     64

// ============================================================================
// СТРУКТУРА КЛИЕНТА
// ============================================================================

typedef struct {
    int  sock;
    int  named;
    int  color_index;
    char name[NAME_LEN];
} Client;

// ============================================================================
// СТРУКТУРА КОМАНДЫ СЕРВЕРА
// ============================================================================

typedef struct {
    const char  *name;
    const char  *description;
    void       (*handler)(Client clients[], int idx, const char *args);
} ServerCommand;

#endif // TYPES_H